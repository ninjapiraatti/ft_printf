#include "ft_printf.h"

void			con_x(t_printf *data, va_list args)
{
	char		*str;

	if (data->star == 1)
		data->fieldwidth = va_arg(args, int);
	data->lli = (long long)va_arg(args, int);
	str = helper_itoa_base((unsigned long long)data->lli, 16, data);
	data->len = ft_strlen(str) + 2;
	if (data->minus == 0)
		helper_print_padding(data);
	ft_putstr("0x");
	ft_putstr(str);
}